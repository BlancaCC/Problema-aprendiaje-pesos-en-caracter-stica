function Muestra(v)
    vr = map(x->round(x, digits=3),v)

    d= 13
    cnt = 1
    l = length(v)
    while(cnt+d < l)
        println("& $(vr[cnt:cnt+d]) \\\\")
        cnt += d
    end
    println("& $(vr[cnt:end])")
    println("Media de valores = $(mean(vr))")
    println("desviación típica = $(std(vr))")
end


